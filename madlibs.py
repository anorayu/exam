# Запросите у пользователя слова для заполнения пропусков в истории
noun = input("Введите существительное: ")
verb = input("Введите глагол: ")
adjective = input("Введите прилагательное: ")

# Сгенерируйте историю с использованием введенных пользователем значений
story = f"Однажды {noun} решил {verb} на самом {adjective} месте."

# Выведите историю на экран
print(story)